export function getRandomMotivator(): Motivator {
  return MOTIVATORS[Math.floor(Math.random() * MOTIVATORS.length)];
}

/** Мотиватор по индексу дня (для стабильного «цитата дня») */
export function getMotivatorOfDay(seed?: number): Motivator {
  const index = seed ?? new Date().getDate();
  return MOTIVATORS[index % MOTIVATORS.length];
}
