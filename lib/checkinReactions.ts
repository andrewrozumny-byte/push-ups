export function getRandomReaction(name: string): string {
  const allMessages = [...HYPE_MESSAGES, ...DEMOTIVATION_MESSAGES] as const;
  const randomMsg = allMessages[Math.floor(Math.random() * allMessages.length)]!;
  return randomMsg(name);
}
