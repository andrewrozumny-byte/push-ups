export function getRandomReaction(name: string): string {
  const allMessages =
    Math.random() > 0.5 ? HYPE_MESSAGES : DEMOTIVATION_MESSAGES;
  const randomMsg =
    allMessages[Math.floor(Math.random() * allMessages.length)]!;
  return randomMsg(name);
}
